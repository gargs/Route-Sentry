#import "LocationPlacemark.h"


@implementation LocationPlacemark

@synthesize coordinate;

- (id)initWithCoordinate:(CLLocationCoordinate2D)coordinateNew {
	coordinate = coordinateNew;
	return self;
}

- (NSString *) title {
	return @"You were here";
}

- (NSString *) subtitle {
	return subtitle;
}

- (void)setSubtitle: (NSString *)subtitleNew {
	if (subtitleNew != subtitle) {
		subtitle = subtitleNew;
	}
}


@end
